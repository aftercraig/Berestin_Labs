# Создание списка
my_list = [1, 2, 3, 4, 5]

# Обращение к элементу по индексу
print(my_list[2])

# Замена элемента
my_list[2] = 10
print(my_list)

# Добавление элемента
my_list.append(6)
print(my_list)

# Удаление элемента
my_list.remove(10)
print(my_list)

# Дублирование списка
duplicated_list = my_list * 2
print(duplicated_list)